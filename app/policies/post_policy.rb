class PostPolicy < ApplicationPolicy
  def update?
    false
  end

  def index?
    logged_in?
  end

  def show?
    scope.where(:id => record.id).exists?
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

end